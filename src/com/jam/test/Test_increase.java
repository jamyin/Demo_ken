public class Test_increase {

	/**
	 * @author by Jam
	 * 2015-2-11 下午2:15:34
	 */
	public static void main(String[] args) {
		/**
		 * @author by Jam
		 * 2015-2-11 下午2:15:34
		 */
		int i=1;
		System.out.println("i++="+(i++));   //i++=1
		System.out.println("i="+i);         //i=2
		
		int j=1;
		System.out.println("++j="+(++j));	//++j=2
		System.out.println("j="+j);			//j=2
        
		 
		
	}
	
}
